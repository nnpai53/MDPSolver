package input;

public class ActionStateMap
{
    private int mainActionIndex;
    private int stateIndex;
    private int actionIndex;

    public int getMainActionIndex()
    {
        return mainActionIndex;
    }
    public void setMainActionIndex(int mainActionIndex)
    {
        this.mainActionIndex = mainActionIndex;
    }
    public int getStateIndex()
    {
        return stateIndex;
    }
    public void setStateIndex(int stateIndex)
    {
        this.stateIndex = stateIndex;
    }
    public int getActionIndex()
    {
        return actionIndex;
    }
    public void setActionIndex(int stateActionIndex)
    {
        this.actionIndex = stateActionIndex;
    }
    
    
}
